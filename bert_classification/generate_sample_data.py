def generate_data(num_per_class_train=80, num_per_class_val=15, num_per_class_test=15):
    """根据模板生成训练、验证和测试数据。"""
    os.makedirs("data", exist_ok=True)

    random.seed(42)

    for split, num_per_class in [("train", num_per_class_train),
                                  ("val", num_per_class_val),
                                  ("test", num_per_class_test)]:
        rows = []
        for label_id, (label_name, templates) in enumerate(LABEL_SAMPLES.items()):
            for _ in range(num_per_class):
                text = random.choice(templates)
                suffix = "".join(random.choices("，。！？的了", k=random.randint(0, 3)))
                rows.append({"text": text + suffix, "label": label_id})

        random.shuffle(rows)
        df = pd.DataFrame(rows)
        path = f"data/{split}.csv"
        df.to_csv(path, index=False, encoding="utf-8-sig")
        print(f"生成 {split} 数据: {len(df)} 条 -> {path}")


if __name__ == "__main__":
    generate_data()
